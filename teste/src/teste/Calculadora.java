package teste;

public class Calculadora {
	
	public Calculadora() {
		
	}
	
	public float soma(float n1, float n2) {
		return n1+n2;
	}
	public float subtração(float n1, float n2) {
		return n1-n2;
	}
	public float multiplicacao(float n1, float n2) {
		return n1*n2;
	}
	public float divisao(float n1, float n2) {
		return n1/n2;
	}

}
